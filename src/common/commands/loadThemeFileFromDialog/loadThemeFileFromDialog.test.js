describe("commands", () => {
  xdescribe("#loadThemeFromDialog", () => {
    xit("loads the specified theme from the dialog box");
    xit("handles the user cancelling out of the dialog box");

    xdescribe("if the loaded theme is new", () => {
      xit("adds the new theme to the store");
      xit("begins editing the new theme");
    });

    xdescribe("if the loaded theme is already in the store", () => {
      xit("updates the existing theme");
      xit("begins editing the new theme");
    });
  });
});
